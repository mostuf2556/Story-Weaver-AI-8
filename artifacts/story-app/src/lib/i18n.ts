/**
 * Returns a translation function for the given locale.
 * Supports {0}, {1}, … positional interpolation.
 * Falls back to the English string if a key is missing in the target locale.
 */
export function getT(locale: Locale): (key: string, ...args: string[]) => string {
  const dict = locale === "he" ? he : en;
  return (key: string, ...args: string[]) => {
    let text = dict[key] ?? en[key] ?? key;
    args.forEach((arg, i) => {
      text = text.replace(`{${i}}`, arg);
    });
    return text;
  };
}
